/**
 * 给你两个字符串 s1 和 s2 ，写一个函数来判断 s2 是否包含 s1 的排列。如果是，返回 true ；否则，返回 false 。
 * 换句话说，s1 的排列之一是 s2 的 子串 。
 * 示例 1：
 * 输入：s1 = "ab" s2 = "eidbaooo"
 * 输出：true
 * 解释：s2 包含 s1 的排列之一 ("ba").
 */
public class Solution_567 {
    //双指针
    public boolean checkInclusion(String s1, String s2) {
        int s1Len = s1.length();
        int s2Len = s2.length();
        if (s1Len > s2Len) {
            return false;
        }
        int left = 0;
        int[] s1Char = new int[26];
        int[] s2Char = new int[26];
        for (int i = 0; i < s1Len; i++) {
            s1Char[s1.charAt(i) - 'a']++;
        }
        for (int right = 0; right < s2Len; right++) {
            int curRight = s2.charAt(right) - 'a';
            s2Char[curRight]++;
            while (s2Char[curRight] > s1Char[curRight]) {
                int curLeft = s2.charAt(left) - 'a';
                s2Char[curLeft]--;
                left++;
            }
            if (right - left + 1 == s1Len) {
                return true;
            }
        }
        return false;
    }
}

